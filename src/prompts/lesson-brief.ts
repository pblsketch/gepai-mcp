/**
 * lesson_brief 프롬프트 — 대화를 수업 설계 브리프로 정리.
 *
 * k-teacher-skills(pblsketch)의 to-lesson-brief를
 * 환경교육·gepai 맥락으로 이식한 것.
 */

export function buildLessonBriefPrompt(): string {
  return `지금까지의 대화를 **수업 설계 브리프**로 정리하세요. 동료 교사와 공유하거나, 다음 AI 작업(다른 세션·다른 도구)에 맥락을 넘기기 위한 한 페이지 문서입니다.

## 반드시 지킬 것 (Non-negotiables)
- 새 질문을 먼저 던지지 않습니다. 이미 나온 맥락만 종합합니다.
- 대화에 없는 정보를 지어내지 않습니다. 모르는 것은 **미확정**으로 표시합니다.
  - 나쁜 예: "학생들은 기본 개념을 알고 있다."
  - 좋은 예: "선행 지식: 미확정. 다음 설계 전에 확인 필요."
- AI가 추정한 부분과 교사가 판단해야 할 부분을 분리합니다.
- 활동보다 목표·학생 맥락·평가 증거가 먼저 보이게 정리합니다.
- 학생 개인정보를 포함하지 않습니다.

## 브리프 구조
\`\`\`
# 수업 설계 브리프: [제목]

1. 수업 의도 — 왜 이 수업인가
2. 학생·학교·지역 맥락 — 학교급/학년/과목, 학생 특성, 활용 자원, 지역 연결점
3. 성취기준 — 확정된 코드와 내용 (gepai 도구로 확인된 것)
4. 참고 자료 — 활용하기로 한 교육청 자료 (출처: 교육청/자료집명/자료 id)
5. 배움의 수레바퀴 구성 — 선택한 단계·연결고리와 차시 배치
6. 중심 질문
7. 학습 증거와 평가 기준 — 무엇을 보면 배웠다고 볼 것인가
8. 예상 오개념
9. 수업 흐름 요약 — 차시별 한 줄
10. 미확정 사항 — 다음 작업 전에 확인할 것
11. 교사 판단 항목 — 목표 우선순위, 평가 방식, 난이도, 민감 주제 처리
12. 다음 AI 작업에 넘길 프롬프트 — 이 브리프를 붙여넣고 이어서 요청할 문장 예시
\`\`\`

대화에서 다루지 않은 항목은 목차에서 빼지 말고 "미확정"으로 남기세요. 그것이 다음 대화의 출발점이 됩니다.`;
}
